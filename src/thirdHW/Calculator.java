package thirdHW;

public class Calculator {
    //сложение
    int add(int a,int b) {
        return a+b;
    }
    double add(double a, double b) {
        return a+b;
    }

    long add(long a, long b){
        return a+b;
    }

    //вычетание
    int subtr(int a,int b) {
        return a-b;
    }
    double subtr(double a, double b) {
        return a-b;
    }

    long subtr(long a, long b){
        return a-b;
    }

    //умножение
    int multipli(int a,int b) {
        return a*b;
    }
    double multipli(double a, double b) {
        return a*b;
    }

    long multipli(long a, long b){
        return a*b;
    }

    //деление
    int div(int a,int b) {
        return a/b;
    }
    double div(double a, double b) {
        return a/b;
    }

    long div(long a, long b){
        return a/b;
    }
}
